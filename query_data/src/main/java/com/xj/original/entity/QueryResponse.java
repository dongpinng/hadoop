package com.xj.original.entity;

public class QueryResponse {
	private String time;
	private Integer pv;
	private Integer uv;
	private Integer orderUV;
	private Integer newUV;
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public Integer getPv() {
		return pv;
	}
	public void setPv(Integer pv) {
		this.pv = pv;
	}
	public Integer getUv() {
		return uv;
	}
	public void setUv(Integer uv) {
		this.uv = uv;
	}
	public Integer getOrderUV() {
		return orderUV;
	}
	public void setOrderUV(Integer orderUV) {
		this.orderUV = orderUV;
	}
	public Integer getNewUV() {
		return newUV;
	}
	public void setNewUV(Integer newUV) {
		this.newUV = newUV;
	}
	
}
